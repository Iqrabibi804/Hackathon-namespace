import { NextResponse } from 'next/server';
import type { NextRequest } from 'next/server';

export async function middleware(req: NextRequest) {
  const res = NextResponse.next();

  // Security headers — production-grade
  res.headers.set('X-Content-Type-Options', 'nosniff');
  res.headers.set('X-Frame-Options', 'DENY');
  res.headers.set('X-XSS-Protection', '1; mode=block');
  res.headers.set('Referrer-Policy', 'strict-origin-when-cross-origin');
  res.headers.set('Permissions-Policy', 'camera=(), microphone=(), geolocation=()');
  res.headers.set(
    'Content-Security-Policy',
    "default-src 'self'; script-src 'self' 'unsafe-eval' 'unsafe-inline'; style-src 'self' 'unsafe-inline'; img-src 'self' data: https:; connect-src 'self' https://eth-mainnet.g.alchemy.com https://api.etherscan.io wss://;"
  );

  // Dashboard is accessible — auth check is handled client-side via wallet connection
  // Remove server-side token check to prevent redirect loops since we use Web3 auth (wagmi/rainbowkit)

  return res;
}

export const config = {
  matcher: ['/dashboard/:path*'],
};
